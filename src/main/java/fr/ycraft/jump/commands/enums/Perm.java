package fr.ycraft.jump.commands.enums;

public final class Perm {
    /**
     * Gives access to admin commands
     */
    public static final String ADMIN = "jump.admin.*";
    /**
     * Give access to the <i>/jump</i> list command
     */
    public static final String ADMIN_LIST = "jump.admin.list";
    /**
     * Gives access to all jump permissions
     */
    public static final String ALL = "jump.*";
    /**
     * Gives access to the <i>/jump create</i> command and edit jumps
     */
    public static final String CREATE = "jump.admin.create";
    /**
     * Enables the player to edit jumps
     */
    public static final String EDIT = "jump.admin.edit";
    /**
     * Allow to places/destroy blocks while in editor
     */
    public static final String EDITOR_INTERACTIONS = "jump.admin.editorinteractions";
    /**
     * Enables player to fly in jumps
     */
    public static final String FLY = "jump.fly";
    /**
     * Give access to the <i>/jump help</i> command
     */
    public static final String HELP = "jump.help";
    /**
     * Gives access to the <i>/jumps</i> command
     */
    public static final String LIST = "jump.list";
    /**
     * Enables player to play a jump and leave jumps
     */
    public static final String PLAY = "jump.play";
    /**
     * Gives access to the <i>/jump reload</i> command
     */
    public static final String RELOAD = "jump.admin.reload";
}
